import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const publicDir = path.resolve(__dirname, '../public');
const outFile = path.resolve(__dirname, '../src/data/publicMediaManifest.ts');

const IMAGE_EXT = /\.(jpe?g|png|gif|webp|avif)$/i;

const GENRE_IDS = ['bw', 'portraits', 'landscape', 'street'];

/**
 * @param {string} relPosix e.g. "gallery/bw"
 * @returns {string[]} public URLs starting with /
 */
function listImageUrls(relPosix) {
  const dir = path.join(publicDir, ...relPosix.split('/'));
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((name) => IMAGE_EXT.test(name) && !name.startsWith('.'))
    .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: 'base', numeric: true }))
    .map((name) => `/${relPosix.split('/').join('/')}/${encodeURI(name)}`);
}

const heroUrls = listImageUrls('hero');
const featuredGalleryUrls = listImageUrls('gallery/featured');

/** @type {Record<string, string[]>} */
const genrePublicUrls = Object.fromEntries(
  GENRE_IDS.map((id) => [id, listImageUrls(`gallery/${id}`)]),
);

const file = `/* eslint-disable */
// Auto-generated by scripts/generate-public-media-manifest.mjs — do not edit.

export const heroUrls: string[] = ${JSON.stringify(heroUrls, null, 2)};

export const featuredGalleryUrls: string[] = ${JSON.stringify(featuredGalleryUrls, null, 2)};

export const genrePublicUrls: Record<string, string[]> = ${JSON.stringify(genrePublicUrls, null, 2)};
`;

fs.writeFileSync(outFile, file, 'utf8');
console.log('Wrote', path.relative(path.resolve(__dirname, '..'), outFile));
